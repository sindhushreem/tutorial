//Normal Function Syntax
var func = function add (a, b) {
	var c = a + b;
	return c;
}

//Arrow function Syntax
var func = (a, b) => {
	var c = a + b;
	return c;
}


//If body of function is of one line, then
var func = (a, b) => a + b;  
